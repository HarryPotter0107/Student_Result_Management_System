    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemistry = jTextField3.getText();
        String maths = jTextField4.getText();
        String os = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        
        try{
          Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Bhanu@0107");
          Statement st = con.createStatement();
          ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
          
          if(rs.next()){
          st.executeUpdate("insert into result(rollno,physics,chemistry,maths,os,dbms)values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+os+"','"+dbms+"')");
          JOptionPane.showMessageDialog(null,"Marks Save successfully");
          setVisible(false);
          new addResult().setVisible(true);
              
          }else{
              JOptionPane.showMessageDialog(null, "Roll Number registered successful!");
          }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

